function colorChoice(color)
{
    switch (color)
    {
        case "red":
            alert("Favorite color is red");
            break;
        case "blue":
            alert("Favorite color is blue");
            break;
        case "green":
            alert("Favorite color is green");
            break;
        case "yellow":
            alert("Favorite color is yellow");
            break;
        case "orange":
            alert("Favorite color is orange");
            break;
        default:
            alert("Error");
            break
    }
}